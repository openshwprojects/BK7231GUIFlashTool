        public static int RF_MAC_OFS = 36;

        public static byte [] getMACFromQio(byte [] data, BKType type, out bool isT1FixRequired)
        {
            isT1FixRequired = false;
            var offset = getRFOffset(type);
            if(offset < 0)
                return new byte[6];
            int mac_ofs = offset + RF_MAC_OFS;
            if(type == BKType.BK7238 && MiscUtils.isFullOf(MiscUtils.subArray(data, mac_ofs, 6), 0xFF))
            {
                mac_ofs = 0x1e3000 + RF_MAC_OFS;
                isT1FixRequired = true;
            }
            return MiscUtils.subArray(data, mac_ofs, 6);
        }

        public static byte[] getRFFromBackup(byte[] data, BKType type, out int addr)
        {
            if(type == BKType.ECR6600)
            {
                addr = getRFOffset(type);
                return MiscUtils.subArray(data, addr, 0x2000);
            }
            addr = -1;
            if(data.Length < 0x1000)
                data = MiscUtils.padArray(data, BK7231Flasher.SECTOR_SIZE);
            for(int i = data.Length - 0x1000; i >= 0; i -= 0x1000)
            {
                if(data[i] == 0x54 && data[i + 1] == 0x4c && data[i + 2] == 0x56 && data[i + 3] == 0x00)
                {
                    // TLV Header found
                    addr = i;
                    return MiscUtils.subArray(data, i, 0x1000);
                }
            }
            return new byte[0];
        }

        public static int getRFOffset(BKType curType)
        {
            switch(curType)
            {
                case BKType.BK7231T:
                case BKType.BK7231U:
                case BKType.BK7238:
                case BKType.BK7252:
                case BKType.BK7252N:
                    return 0x1e0000;
                case BKType.BK7231N:
                case BKType.BK7231M:
                    return 0x1d0000;
                case BKType.BK7236:
                    return 0x3fe000;
                case BKType.BK7258:
                    return 0x7fe000;
                case BKType.ECR6600:
                    return 0x1fe000;
                default:
                    return -1;
            }
        }
